package pt.unl.fct.di.apdcindiv.util;

public class User {
	
	public String name;
	public String NIF;
	public String url;
	public String address;
	public String phone;
	
	public User() {}
	
	public User(String name, String NIF, String url, String address, String phone) {
		this.name = name;
		this.NIF = NIF;
		this.url = url;
		this.address = address;
		this.phone = phone;
	}

}
